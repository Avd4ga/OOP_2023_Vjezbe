package LabWeek2;

public class Lab2_7 {
    public static void main(String[] args) {


        printStars(5);
        printStars(6);
    }

    public static void printStars(int amount) {

        for (int i = 0; i <= amount; i++) {
            System.out.print("*");
        }
        System.out.println();

    }}
